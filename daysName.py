a=int(input("Enter day number of week : "))

if(a==1):
    print("Day is Monday")
elif(a==2):
    print("Day is Tuesday")
elif(a==3):
    print("Day is Wednesday")
elif(a==4):
    print("Day is Thursday")
elif(a==5):
    print("Day is Friday")
elif(a==6):
    print("Day is Saturday")
elif(a==7):
    print("Day is Sunday")
else:
    print("Invalid day")

